import type { AccidentReport } from '@/types/accident-report'

let _store: AccidentReport[] | null = null

function getStore(): AccidentReport[] {
  if (!_store) _store = [...SEED]
  return _store
}

export function getMockAccidentReports(): AccidentReport[] {
  return getStore()
}

export function upsertMockAccidentReport(r: AccidentReport): void {
  const store = getStore()
  const idx = store.findIndex((x) => x.accidentID === r.accidentID)
  if (idx >= 0) store[idx] = r
  else _store = [...store, r]
}

export function removeMockAccidentReport(id: number): void {
  _store = getStore().filter((r) => r.accidentID !== id)
}

