let _store: VendorPayment[] | null = null

function getStore(): VendorPayment[] {
  if (!_store) _store = [...SEED]
  return _store
}

export function getMockVendorPayments(): VendorPayment[] {
  return getStore()
}

export function upsertMockVendorPayment(r: VendorPayment): void {
  const store = getStore()
  const idx = store.findIndex((x) => x.vendorPaymentID === r.vendorPaymentID)
  if (idx >= 0) store[idx] = r
  else _store = [...store, r]
}

export function removeMockVendorPayment(id: number): void {
  _store = getStore().filter((r) => r.vendorPaymentID !== id)
}

