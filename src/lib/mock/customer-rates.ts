let _store: CustomerRate[] | null = null

function getStore(): CustomerRate[] {
  if (!_store) _store = [...SEED]
  return _store
}

export function getMockCustomerRates(): CustomerRate[] {
  return getStore()
}

export function upsertMockCustomerRate(r: CustomerRate): void {
  const store = getStore()
  const idx = store.findIndex((x) => x.customerRateID === r.customerRateID)
  if (idx >= 0) store[idx] = r
  else _store = [...store, r]
}

export function removeMockCustomerRate(id: number): void {
  _store = getStore().filter((r) => r.customerRateID !== id)
}

