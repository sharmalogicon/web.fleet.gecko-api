let _store: InsuranceClaim[] | null = null

function getStore(): InsuranceClaim[] {
  if (!_store) _store = [...SEED]
  return _store
}

export function getMockInsuranceClaims(): InsuranceClaim[] {
  return getStore()
}

export function upsertMockInsuranceClaim(r: InsuranceClaim): void {
  const store = getStore()
  const idx = store.findIndex((x) => x.claimID === r.claimID)
  if (idx >= 0) store[idx] = r
  else _store = [...store, r]
}

export function removeMockInsuranceClaim(id: number): void {
  _store = getStore().filter((r) => r.claimID !== id)
}

