import type { TireProfile } from '@/types/tire-profile'

let _store: TireProfile[] | null = null

function getStore(): TireProfile[] {
  if (!_store) _store = [...SEED]
  return _store
}

export function getMockTireProfiles(): TireProfile[] {
  return getStore()
}

export function upsertMockTireProfile(r: TireProfile): void {
  const store = getStore()
  const idx = store.findIndex((x) => x.tireProfileID === r.tireProfileID)
  if (idx >= 0) store[idx] = r
  else _store = [...store, r]
}

export function removeMockTireProfile(id: number): void {
  _store = getStore().filter((r) => r.tireProfileID !== id)
}

